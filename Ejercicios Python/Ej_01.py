''' Escribe un programa que pida por teclado un número entero mostrando un mensaje
oportuno. A continuación escribe en pantalla el número leído, el doble del número leído y
el triple del mismo.'''

num = int(input("Introduzca un numero: "))
print('El numero introducido es:', num)
print('El doble del numero introducido es:', num * 2)
print('El triple del numero introducido es:', num * 3)

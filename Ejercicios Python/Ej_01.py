''' Escribe un programa que pida por teclado un número entero mostrando un mensaje
oportuno. A continuación escribe en pantalla el número leído, el doble del número leído y
el triple del mismo.'''

a = int(input("Introduzca un numero: "))
print('El numero introducido es:', a)
print('El doble del numero introducido es:', a * 2)
print('El triple del numero introducido es:', a * 3)

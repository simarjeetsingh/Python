''' Escribe un programa que pida por teclado el radio de una circunferencia, y que a
continuación calcule y escriba en pantalla la longitud de la circunferencia y el área del
círculo.'''

# Repite el ejercicio 2 utilizando una constante que represente el valor de PI.


pi=3.14
radio = float(input("Introduzca el radio de la cirucmferencia: "))
print('La longitud de la circumferecia es:', 2*pi*radio)
print('El área del circulo es:', pi*radio**2)


